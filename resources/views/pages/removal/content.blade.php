        <h1 class="removal-title">INK</h1>

        <p class="removal-text">Du bist nicht allein; Jeder zweite Tätowierte ist genauso betroffen. Die weltweit effizienteste und hautschonendste
        Entfernung der Tätowierung erfolgt mittels dafür bestimmte Lasersysteme. Je nach Farbe kommen verschiedene Wellenlängen
        zum Einsatz, welche die Farbpigmente unter der Haut, absorbieren. Durch die kurzen Lichtimpulse des Lasers werden die
        einzelnen Pigmente in feinste Partikel zerstäubt und vom eigenen Lymphsystem im Körper abgetragen. Das Resultat ist das
        sichtbare Verschwinden der Tätowierung. Während diesem Prozess bleibt das Hautgewebe und die Hautzellen unbeschadet.</p>

